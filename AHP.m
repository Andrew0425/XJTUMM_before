function consistency_ratio = ahp_consistency_check(A)
    % A: AHP决策矩阵

    % 计算权重向量
    n = size(A, 1);
    w = zeros(n, 1);
    for i = 1:n
        w(i) = prod(A(i, :))^(1/n);
    end
    w = w / sum(w);

    % 计算一致性指标CI
    lambda_max = max(eig(A));
    CI = (lambda_max - n) / (n - 1);

    % 根据随机一致性指数RI，计算一致性比例CR
    RI_values = [0, 0, 0.52, 0.89, 1.11, 1.25, 1.35, 1.40, 1.45, 1.49,1.52];
    RI = RI_values(n);
    CR = CI / RI;

    % 输出一致性比例
    fprintf('一致性比例 CR = %.4f\n', CR);

    consistency_ratio = CR;
end
